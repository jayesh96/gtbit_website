<p>uru Gobind Singh Indraprastha University, established by the Government of Delhi in 1998 is a dynamic and growth oriented university, set up to facilitate and promote studies & research with a focus on professional education in emerging areas of higher education in disciplines of engineering, technology, management studies, medicine, pharmacy, nursing, education, law etc aiming to produce quality man power. The objective was to achieve excellence in these and related fields and to equip the students to become effective professionals with the best career prospects to meet the emerging global and industrial needs of society in the coming century. The University visualises its role as a catalyst for developing the technical skills of the students with a view to meet the needs of the industry. It awards various degrees like: B.Tech/M.Tech , MCA , M.Sc. , B.Arch. , BCA/MCA , BBA/MBA, BMC , BCAM , B.Pharma , BPT , BHMCT , B.Ed. , LLB(Hons.), MAHM , MCPHM , MHRPD , BHMS, MBBS, PhD etc. Admission to these programmes is through a Common Entrance Test. The University is recognized by the University Grants Commission (UGC) of India under section 12B of the UGC Act.<br/><br/>

