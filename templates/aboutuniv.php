The University in its endeavour to ensure excellence at all levels of professional education, intends to maintain high standards in terms of teaching, research, faculty and infrastructure. In order to cultivate habits of regular and systematic study among the students, the University has evolved a system of performance appraisal which is based on a combination of continuous internal assessment and end-term evaluation. It also expects every student of the affiliated institutions to be responsible for regularity of attendance in the institutions and to achieve a certain minimum level of performance and progress.<br/><br/>

