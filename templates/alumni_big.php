<div class="info col-lg-10">
<p>He graduated in 2007 from the institute and is presently pursuing MBA from IIM Lucknow.<br/><br/>

Here's an account of an interview with him about his exhilarating experiences in the college as well as the interaction with the world....<br/><br/></p>
<ul>
<li><p>
What is your present position? <br/>
Member, MBA at IIM Lucknow (that’s right, they don’t call or treat us as Students any more ; )<br/></p></li>

<li><p>
What are your hobbies?<br/>
Elocution, Travelling, Wasting time and doing NOTHING<br/></p></li>

<li><p>
What have been your travel experiences so far?<br/>
Loads.. I have been lucky enough to have been able to see a lot of places in my life. I have been to most of the hill stations and beaches you would find in India. That includes Srinagar, Jammu, Manali (ooh.. those apples), Dharmshala, Mussorie, Kodiacanal, Otty, etc etc,, The beaches have been pretty good, but I guess Port Blair (Andaman and Nicobar Islands) and Jaganath Puri stand out. I also have been to Singapore, Hong Kong and Nepal.<br/></p></li>

<li><p>
How was your college life? <br/>
Pretty amazing. Never had more free time in life, and I guess I never would. Was the best part of my life till now. Formed great associations, friendships, achievements and memories.<br/></p></li>

<li><p>
Who was your first crush? <br/>
Expecting me to remember that would be a little too much, wont it? :P<br/></p></li>

<li><p>
Did your college help you in pursuing further studies? <br/>
It helped as the extra curricular activities I was able to participate in were of immense help in helping me know myself a LOT better, and also improve on the traits I wanted to develop. And even though I value the knowledge too, but at the end of the day, knowledge doesn’t stick with you for too long, but the skills you develop remain for a lifetime. So instead of trying to just get great marks, also try to have some great experiences.<br/></p></li>

<li><p>
Which ones were your favorite classes?<br/>
C++, Data Structures, DBMS, and DCN<br/></p></li>

<li><p>
Did you always get best grades in your favorite classes or did you excel in other 
classes instead?<br/>
No, I never did. (Except C++ I guess). Yet, they were the subjects I quoted in all my interviews, as I felt I had more knowledge about them. This again proves the point that marks don’t necessarily translate into knowledge.<br/></p></li>

<li><p>
What have been your noted projects?<br/>
Have there been any??? I guess the excel spreadsheet for Bulls N Bears created at GATES 2k5 remains my best project till date, as this was something I felt really good about. And guess what, it was used for 3 consecutive years at GATES, in spite of the fact that I had no prior knowledge of either finance or MS Excel.
<br/></p></li>
<li><p>

Which awards have you received so far? <br/>
The top few I would like to name would be the ones for topping the 1st and 2nd Semester, Certificate of Appreciation from our former Director, Maj. Gen. (Retd.) S. S. Chahal, and being the Student Representative Literary.<br/></p></li>

<li><p>
What has been your vision in life?<br/>
I guess 4/6.. that’s true, as I wasnt ever too clear about what I wanted to do or where I wanted to go. Fortunately, I got a passion to work for in the form of MBA. Somehow, Hindsight is always 20/20, and now it appears that things worked out decently well for me.<br/></p></li>

<li><p>
Would you like to say a few words for your alma mater?<br/>
GTBIT is a great college which has some great individuals as its students and alumni. The responsibility of making it greater lies with the students alone, as I don’t think that figures on the list of priorities of the management. They may even try to discourage you, but if you can fight them for what is right, and show some character, you would be a winner for sure.<br/></p></li> </ul>
<p>
I hope this helps you all and would motivate some of you to do something that even the alumni of GTBIT would be proud to quote. 

In case anyone needs to contact me, feel free to do so at ankitdhingra@gmail.com </p>
