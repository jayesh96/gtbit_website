<p>GTBIT has been conscious of its obligation towards the placement of the students. Our placement cell, which is headed by a senior faculty member, is further strengthening this activity by way of regular industrial visits of the students and a continuous interaction with industries and business houses</p>
<h3>Industrial Visits</h3>
<p>Visits are organized to various industries to help students expand their academic and technical horizons. These are excellent opportunities for students to gain exposure to real-life work environments and cultures.
</p>
<h3>Projects</h3>
<p>The students take up compulsory Industry related projects as part of the third and fourth year curriculum which provides them with an opportunity for active interaction with the corporate world and gain practical hands-on experience
</p>
<h3>Summer Training</h3>
<p>As a part of the curriculum students undergo two different training programmes :</p>
<ul><li>
In-house training programme : six weeks (after 4th semester)</li>
<li>Summer training : eight weeks (after 6th semester)</li></ul><br/><p>
All the students after the completion of the 4th semester undergo a six week in-house training programme during the summer break (June-July). The summer training program after the 6th semester helps students interact with the industry and get exposure in their respective fields.
</p>
<h3>Placement Cell</h3>
<p>The thrust of the Training and Placement department is towards providing the students a platform for interaction with the corporate world and also to help improve the all-round personality of the students. These aims are achieved through lectures, workshops, seminars, information about career opportunities, aptitude tests, communication enhancement lectures, group discussions, interviews, pre-placement talks, debates, preparing professional curriculum vitae etc</p>
